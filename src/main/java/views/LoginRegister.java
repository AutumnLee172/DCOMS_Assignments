    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String email = txtEmailLogin.getText();
        String passwords = txtPasswordLogin.getText();
        try {
            RMIinterface Obj = (RMIinterface)Naming.lookup("rmi://localhost:1040/KGF");
           if(Obj.customer_login(email, passwords)){
                lblLoginResult.setText("you are logged in.");
                Customer cm = Obj.customer_setup(email);
                Home hm = new Home(cm);
                hm.setVisible(true);
                this.dispose();
            }else{
                lblLoginResult.setText("Incorrect email or passwords.");
            }
        } catch (NotBoundException ex) {
            Logger.getLogger(LoginRegister.class.getName()).log(Level.SEVERE, null, ex);
        } catch (MalformedURLException ex) {
            Logger.getLogger(LoginRegister.class.getName()).log(Level.SEVERE, null, ex);
        } catch (RemoteException ex) {
            Logger.getLogger(LoginRegister.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(LoginRegister.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnLoginActionPerformed

