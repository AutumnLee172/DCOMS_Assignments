    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        try {
            String result = "";
            RMIinterface Obj = (RMIinterface)Naming.lookup("rmi://localhost:1040/KGF");
            String name = txtUsername.getText();
            String password = txtPassword.getText();  
            String Cpassword = txtConfirmPassword.getText();
            String email = txtEmail.getText();
            if(password.equals(Cpassword)){
                 result = Obj.customer_register(email, name,password);
            }else{
                result = "Mismatched Passwords";
            }
            lblResult.setText(result);
        } catch (NotBoundException | MalformedURLException | RemoteException ex) {
            Logger.getLogger(LoginRegister.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnCreateActionPerformed

